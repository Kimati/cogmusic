    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,['title'=>'required','release'=>'required']);
        $song=new FullAlbum;
        $song->album_name=$request->input('title');
        $song->date_released=$request->input('release');
        $song->youtube_link=$request->input('youtube');
        $song->save();
        return redirect()->back()->with('success','Song Posted successfully.');
    }

