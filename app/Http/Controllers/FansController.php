    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,['Fname'=>'required','Sname'=>'required','phone'=>'min:10|max:10','email'=>'email','comment'=>'required']);
        $comment = new FanComment;
        $comment->Fname = $request->input('Fname');
        $comment->Sname = $request->input('Sname');
        $comment->Phone = $request->input('phone');
        $comment->Email = $request->input('email');
        $comment->Comment = $request->input('comment');
        $comment->save();
        return redirect('comments')->with('success','Comment Posted successfully');
    }

